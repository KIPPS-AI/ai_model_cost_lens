// ─────────────────────────────────────────────────────────────────────────────
// Model data — source of truth for all pricing and capability information.
//
// HOW TO ADD A NEW PROVIDER / MODEL:
//   1. Add a new object to MODEL_PRICING below.
//   2. Use any string for `provider` — filters derive it dynamically.
//   3. Use one of the Modality values for `modality`.
//   4. That's it. No other files need editing.
// ─────────────────────────────────────────────────────────────────────────────

export type Modality = "text" | "multimodal" | "audio" | "image" | "video";

// Provider is intentionally a plain string so any new provider can be added
// without touching this type. The union below is kept only for documentation.
export type Provider = string;

export interface ModelPricing {
	id: string;
	displayName: string;
	apiName: string;
	provider: Provider;
	modality: Modality;
	// Token pricing (text / multimodal LLMs)
	inputPricePer1M: number; // $ per 1M input tokens  (0 if not applicable)
	outputPricePer1M: number; // $ per 1M output tokens (0 if not applicable)
	cachedInputPricePer1M?: number | null;
	contextLength: number; // tokens (0 if not applicable)
	// Capabilities
	supportsTemperature: boolean;
	supportsTopK: boolean;
	supportsFunctionCalling: boolean;
	// Audio pricing
	sttPricePer1Min?: number; // $ per minute  (STT)
	ttsPricePer1KChars?: number; // $ per 1K chars (TTS)
	// Image pricing
	imagePricePerImage?: number; // $ per image generated
	imageResolution?: string; // e.g. "1024×1024"
	// Video pricing
	videoPricePerSecond?: number; // $ per second of video
	videoResolution?: string; // e.g. "720p"
	// Meta
	description?: string;
}

// ── Helper types derived from the data ───────────────────────────────────────
export type DefaultConfig = {
	modelId: string;
	taskType: TaskType;
	aiComponents: AIComponent[];
	systemPromptTokens: number;
	userPromptTokens: number;
	assistantResponseTokens: number;
	conversationHistory: number;
	historyGrowthRate: number;
	enablePromptCaching: boolean;
	reasoningMultiplier: number;
	audioInputMinutes: number;
	audioOutputMinutes: number;
	sttModelId: string;
	ttsModelId: string;
	monthlyUsers: number;
	imageModelId: string;
	videoModelId: string;
	imagesPerConversation: number;
	videoSecondsPerConversation: number;
};

export type TaskType = "voice-call" | "chatbot" | "crm" | "image-generation" | "video-generation" | "custom";

export type AIComponent = "llm" | "stt" | "tts" | "streaming";

export interface CalculatorConfig {
	taskType: TaskType;
	aiComponents: AIComponent[];
	modelId: string;
	// Token parameters (stored as tokens, displayed as tokens in UI)
	systemPromptChars: number; // system prompt size in tokens
	avgUserInputChars: number; // user input tokens per message
	avgResponseChars: number; // assistant response tokens per turn
	conversationTurns: number; // number of back-and-forth turns
	toolCallsPerConversation: number; // tool calls per conversation
	toolDescriptionChars: number; // tool schema size in tokens
	ragTokens: number; // retrieved context tokens injected per turn
	includeHistoryGrowth: boolean;
	usePromptCaching: boolean;
	reasoningMultiplier: number;
	avgCallDurationMinutes: number;
	wordsPerMinute: number;
	sttModelId: string;
	ttsModelId: string;
	monthlyUsers: number;
	// Image / video
	imageModelId: string;
	videoModelId: string;
	imagesPerConversation: number;
	videoSecondsPerConversation: number;
}

